function add_error_cross(fig, xdata, ydata, color, lw)

figure(fig)

line(mean(xdata) + [-1,1]*sem(xdata), ...
    mean(ydata) * [1,1], ...
    'linewidth', lw, ...
    'color', color)

line(mean(xdata) * [1,1],...
    mean(ydata) + [-1,1]*sem(ydata), ...
    'linewidth', lw, ...
    'color', color)

end